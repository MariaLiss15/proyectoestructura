    public void listarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");

        Object datos[][] = new Object[1][2];
        cargos = ListaDin_Cargos.consultar();

        for (int i = 0; i < cargos.Cantidad(); i++) {
            Cargo objCargo = (Cargo) cargos.Obtener(i);
            datos[0][0] = objCargo.getId();
            datos[0][1] = objCargo.getNombre();
            modelo.addRow(datos[0]);
        }

        this.tblDatos.setModel(modelo);
        this.tblDatos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
    }

