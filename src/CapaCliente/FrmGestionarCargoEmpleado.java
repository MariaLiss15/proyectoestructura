    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            int id = Integer.parseInt(txtId.getText());
            String nombre = txtNombre.getText();
            Cargo objCargo = new Cargo();
            objCargo.setId(id);
            objCargo.setNombre(nombre);
            ListaDin_Cargos.adicionar(objCargo);

            JOptionPane.showMessageDialog(this, "Registro exitoso", "Mensaje de Sistema", JOptionPane.INFORMATION_MESSAGE);
            listarTabla();

            txtId.setText("");
            txtNombre.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "ERROR: \n" + ex.getLocalizedMessage(), "SISTEMA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int pos = tblDatos.getSelectedRow();

        if (cargos.Cantidad() > 0) {
            if (pos >= 0) {
                int rptaUser = JOptionPane.showConfirmDialog(this, "¿Esta seguro de eliminar?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE); //0=SÍ, 1=NO
                if (rptaUser == 0) {
                    cargos.Eliminar(pos);
                    JOptionPane.showMessageDialog(this, "El registro se eliminó correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);
                    listarTabla();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un registro", "Mensaje del Sistema", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "No hay ningún cargo para eliminar", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed


